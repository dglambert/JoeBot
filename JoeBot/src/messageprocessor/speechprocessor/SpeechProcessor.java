package messageprocessor.speechprocessor;


import joebot.JoeBot;
import speech.Speech;



public class SpeechProcessor
{
    ////////////////////////////////////////////////////////////////////////////////////////////////
    // Attributes //////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    JoeBot joeBot;
    Speech speech;

    ////////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public SpeechProcessor(JoeBot joeBot, Speech speech)
    {
        this.joeBot = joeBot;
        this.speech = speech;
    }


    ////////////////////////////////////////////////////////////////////////////////////////////////
    // Methods /////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public void processSpeech()
    {

        if(speech.containsGreeting())
        {
            greeting();
        }



    }

    private void greeting()
    {
        speech.getVocab();
    }


}
