package messagecontents.hostname;

class HostName implements HostNameSpec
{
    ///////////////////////////////////////////////////////////////////
    // Attributes /////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    String stringHostName;
    
	
    ///////////////////////////////////////////////////////////////////
    // Constructors ///////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    HostName(String strHostName)
    {
    	setHostName(strHostName);
    }
    
    
    ///////////////////////////////////////////////////////////////////
    // Get Methods ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    public String getHostName()
    {
    	return stringHostName;
    }
    
    
    ///////////////////////////////////////////////////////////////////
    // Set Methods ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    public void setHostName(String strHostName)
    {
    	stringHostName = strHostName;
    }
}