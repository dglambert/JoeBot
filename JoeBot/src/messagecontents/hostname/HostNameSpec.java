package messagecontents.hostname;

public interface HostNameSpec
{
    ///////////////////////////////////////////////////////////////////
    // Get Methods ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    /**
	* Returns the String value of this HostName
	*
	* @returns - String value of this HostName
	*/
    public String getHostName();
    
    
    ///////////////////////////////////////////////////////////////////
    // Set Methods ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    /**
	* Sets the String value of this HostName
	*
	* @param strHostName - a hostname represented as a String
	*/
    public void setHostName(String strHostName);
}