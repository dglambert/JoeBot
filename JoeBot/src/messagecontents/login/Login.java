package messagecontents.login;

class Login implements LoginSpec
{
    ///////////////////////////////////////////////////////////////////
    // Attributes /////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    String stringLogin;
    
	
    ///////////////////////////////////////////////////////////////////
    // Constructors ///////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    Login(String strLogin)
    {
    	setLogin(strLogin);
    }
    
    
    ///////////////////////////////////////////////////////////////////
    // Get Methods ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    public String getLogin()
    {
    	return stringLogin;
    }
    
    
    ///////////////////////////////////////////////////////////////////
    // Set Methods ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    public void setLogin(String strLogin)
    {
    	stringLogin = strLogin;
    }
}