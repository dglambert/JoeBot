package messagecontents.channel;

public interface ChannelSpec
{
    ///////////////////////////////////////////////////////////////////
    // Get Methods ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    /**
	* Returns the String value of this Channel
	*
	* @returns - String value of this Channel
	*/
    public String getChannel();
    
    
    ///////////////////////////////////////////////////////////////////
    // Set Methods ////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////
    
    /**
	* Sets the String value of this Channel
	*
	* @param strChannel - a channel represented as a String
	*/
    public void setChannel(String strChannel);
}